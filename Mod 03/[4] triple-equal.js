console.log(1 == 2, 1 === 2);
console.log(true == 1, true === 1);
console.log(false == 0, false === 0);
console.log(null == undefined, null === undefined);
console.log(0 == '', 0 === '');
console.log('5' == 5, '5' === 5);
console.log('' == false, '' === false);
console.log(NaN == NaN, NaN === NaN);